class Sorter {
    /**
     * @param {Array} list List to be sorted
     * @returns {undefined}
     */
    constructor(list) {
        this.list = list;
    }

    /**
     * Returns a sorted list
     *
     * @returns {Array} The sorted list
     *
     */
    sort() {
        return this.list;
    }
}

module.exports = Sorter;
